function assertParity(domain, localeMaps) {
  const locales = Object.keys(localeMaps);
  const base = locales[0];
  const baseKeys = flattenKeys(localeMaps[base]);
  for (const locale of locales.slice(1)) {
    const keys = flattenKeys(localeMaps[locale]);
    const missing = baseKeys.filter((k) => !keys.includes(k));
    const extra = keys.filter((k) => !baseKeys.includes(k));
    if (missing.length || extra.length) {
      throw new Error(
        `[${domain}] key parity failed for ${locale}: missing=${missing.join(",")} extra=${extra.join(",")}`
      );
    }
  }
}

function flattenKeys(obj, prefix = "") {
  /** @type {string[]} */
  const keys = [];
  for (const [k, v] of Object.entries(obj)) {
    const path = prefix ? `${prefix}.${k}` : k;
    if (v && typeof v === "object" && !Array.isArray(v)) {
      keys.push(...flattenKeys(v, path));
    } else {
      keys.push(path);
    }
  }
  return keys;
}

for (const [domain, localeMaps] of Object.entries(catalogs)) {
  assertParity(domain, localeMaps);
  for (const [locale, data] of Object.entries(localeMaps)) {
    const file = join(root, locale, `${domain}.json`);
    mkdirSync(dirname(file), { recursive: true });
    writeFileSync(file, `${JSON.stringify(data, null, 2)}\n`, "utf8");
  }
}

