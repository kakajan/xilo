def build_locale(
    fa_keys: list[str],
    fa_map: dict[str, str],
    en_map: dict[str, str],
    overlay: dict[str, str],
) -> dict[str, str]:
    """Prefer overlay, then English, then Persian — never leave a key missing."""
    out: dict[str, str] = {}
    for key in fa_keys:
        if key in overlay:
            out[key] = overlay[key]
        elif key in en_map:
            out[key] = en_map[key]
        else:
            out[key] = fa_map[key]
    # Append new keys that aren't in original fa yet (will be patched into fa too)
    for key, value in overlay.items():
        if key not in out:
            out[key] = value
    return out


