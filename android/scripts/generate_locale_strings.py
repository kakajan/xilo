def parse_strings(path: Path) -> dict[str, str]:
    text = path.read_text(encoding="utf-8")
    return dict(re.findall(r'<string name="([^"]+)">(.*?)</string>', text, re.S))


def unescape_xml(value: str) -> str:
    return (
        value.replace(r"\'", "'")
        .replace("&lt;", "<")
        .replace("&amp;", "&")
    )


def escape_xml(value: str) -> str:
    value = unescape_xml(value)
    value = value.replace("&", "&amp;").replace("<", "&lt;")
    if "'" in value and not (value.startswith('"') and value.endswith('"')):
        value = value.replace("'", r"\'")
    return value


def write_strings(path: Path, mapping: dict[str, str], comment: str) -> None:
    lines = ["<resources>", f"    <!-- {comment} -->"]
    for key, value in mapping.items():
        lines.append(f'    <string name="{key}">{escape_xml(value)}</string>')
    lines.append("</resources>\n")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines), encoding="utf-8")


