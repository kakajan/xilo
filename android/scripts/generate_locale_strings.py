    fa_out = dict(fa_map)
    fa_out.update(NEW["fa"])
    # Preserve original order then new keys
    fa_ordered: dict[str, str] = {}
    for key in fa_keys:
        fa_ordered[key] = fa_out[key]
    for key in NEW["fa"]:
        if key not in fa_ordered:
            fa_ordered[key] = fa_out[key]

    all_keys = list(fa_ordered.keys())
    en_ordered = build_locale(all_keys, fa_ordered, en_map, NEW["en"])
    # Ensure reply/comment keys from NEW["en"] are present
    for key, value in NEW["en"].items():
        en_ordered[key] = value

    ar_ordered = build_locale(all_keys, fa_ordered, en_ordered, AR)
    for key, value in NEW["en"].items():
        ar_ordered.setdefault(key, AR.get(key, value))
    for key, value in AR.items():
        ar_ordered[key] = value

    ru_ordered = build_locale(all_keys, fa_ordered, en_ordered, RU)
    for key, value in RU.items():
        ru_ordered[key] = value

